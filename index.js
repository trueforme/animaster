function getTransform(translation, ratio) {
    const result = [];
    if (translation) {
        result.push(`translate(${translation.x}px,${translation.y}px)`);
    }
    if (ratio) {
        result.push(`scale(${ratio})`);
    }
    return result.join(' ');
}

function animaster() {

    function fadeIn(element, duration) {
        element.style.transitionDuration = `${duration}ms`;
        element.classList.remove('hide');
        element.classList.add('show');
    }

    // Функция, скрывающая элемент, аналогичная fadeIn
    function fadeOut(element, duration) {
        element.style.transitionDuration = `${duration}ms`;
        element.classList.remove('show');
        element.classList.add('hide');
    }

    function move(element, duration, translation) {
        element.style.transitionDuration = `${duration}ms`;
        element.style.transform = getTransform(translation, null);
    }

    function scale(element, duration, ratio) {
        element.style.transitionDuration = `${duration}ms`;
        element.style.transform = getTransform(null, ratio);
    }

    function moveAndHide(element, duration){
        move(element, 2 / 5 * duration, {x : 100, y: 20});
        setTimeout(fadeOut, duration * 2 / 5, element, duration * 3 / 5);
    }

    function showAndHide(element, duration){
        fadeIn(element, duration * 1 / 3);
        setTimeout(fadeOut, duration * 2 / 3, element, duration * 1 / 3);
    }

    function heartBeating(element){
        /*for (let i = 0; i <= 100000; i += 2){
            setTimeout(scale, i * 500, element, 500, 7 / 5);
            setTimeout(scale, (i + 1) * 500, element, 500, 5 / 7);
        }
        */
        setInterval(() => {
            scale(element, 500, 1.4);
            setTimeout(() => scale(element, 500, 5 / 7), 500);
        }, 1000);
    }

    return {
        fadeIn: fadeIn,
        fadeOut: fadeOut,
        move: move,
        scale: scale,
        moveAndHide: moveAndHide,
        showAndHide: showAndHide,
        heartBeating: heartBeating,
    }
}